print("Comparison Operator :- \n")

# less-then & greater-then Operator
print("Less-then & Greater-then Operator : ")
print("Condition 1st : ",3 > 2)
print("Condition 2nd : ",45 < 13)

# greater-then equal-to & less-then equal-to Operator
print("\nLess-then Equal-to & Greater-then Equal-to Operator : ")
print("Condition 1st : ",68 >= 68)
print("Condition 2nd : ",54 <= 54)
print("Condition 3rd : ",56 <= 66)
print("Condition 4th : ",98 <= 19)

# Equal-to equal-to Operator
print("\nEqual-to Equal-to Operator : ")
print("Condition 1st : ",51 == 78)
print("Condition 2nd : ",48 == 48)

# Not-Equal to Operatoe
print("\nNot Equal-to Operator : ")
print("Condition 1st : ",43 != 44)
print("Condition 2nd : ",88 != 88)